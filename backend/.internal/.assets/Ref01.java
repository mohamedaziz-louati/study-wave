    public static final class Algorithms {
        /** Classic binary search (iterative). */
        public static int binarySearch(int[] arr, int target) {
            int lo = 0, hi = arr.length - 1;
            while (lo <= hi) {
                int mid = lo + (hi - lo) / 2;
                int v = arr[mid];
                if (v == target) return mid;
                if (v < target) lo = mid + 1; else hi = mid - 1;
            }
            return -1;
        }

        /** Simple quicksort (in-place). */
        public static void quickSort(int[] a) { quickSort(a, 0, a.length - 1); }
        private static void quickSort(int[] a, int l, int r) {
            if (l >= r) return;
            int i = l, j = r, p = a[(l + r) >>> 1];
            while (i <= j) {
                while (a[i] < p) i++;
                while (a[j] > p) j--;
                if (i <= j) {
                    int t = a[i]; a[i] = a[j]; a[j] = t;
                    i++; j--;
                }
            }
            if (l < j) quickSort(a, l, j);
            if (i < r) quickSort(a, i, r);
        }

        /** Fibonacci with memoization. */
        public static long fibonacci(int n) {
            long[] dp = new long[Math.max(2, n + 1)];
            dp[0] = 0; dp[1] = 1;
            for (int i = 2; i <= n; i++) dp[i] = dp[i - 1] + dp[i - 2];
            return dp[n];
        }
    }

